export function getDropsByBrand(brandSlug: string): Drop[] {
  return drops.filter(d => d.brandSlug === brandSlug)
}

export function getLatestDrops(count = 3): Drop[] {
  return [...drops]
    .sort((a, b) => new Date(b.dropDate).getTime() - new Date(a.dropDate).getTime())
    .slice(0, count)
}
